// && (AND) || (OR) ! (NOT)
public class Logical {
    public static void main (String args[]){
        System.out.println( (3>2) && (5>0)) ; // both condition are true than answer is true 
        System.out.println( (3>2) && (5<0)) ;

        // || one condition is true than answer is TRUE . 
        System.out.println( (3>2) || (5<0)) ;
        System.out.println( (3>2) || (5<0)) ;

        // If condition is right than it give the answer is false and if the condition is true it give the answer of true .
        System.out.println( !(3>2)) ;
        System.out.println( !(4%2==0)) ;
        
        
        


        
    }
}
